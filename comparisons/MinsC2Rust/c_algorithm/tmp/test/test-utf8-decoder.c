// clang-format on
static uint32_t inline decode_utf8(uint32_t *state, uint32_t *codepoint,
                                   uint8_t byte) {
  uint32_t type = utf8d[byte];
  *codepoint = (*state != UTF8_ACCEPT) ? (byte & 0x3fu) | (*codepoint << 6)
                                       : (0xff >> type) & (byte);
  *state = utf8d[256 + *state * 16 + type];
  return *state;
}

